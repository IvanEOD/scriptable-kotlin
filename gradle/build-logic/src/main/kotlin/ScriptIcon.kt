    companion object {
        private val enumNames by lazy { values().map { it.name } }
        private val valueNames by lazy { values().map { it.value } }

        fun valueOrNull(
            input: Any,
        ): ScriptIcon? =
            when (input) {
                is ScriptIcon -> input
                is String -> fromStringOrNull(input)
                else -> null
            }

        private fun fromStringOrNull(
            value: String
        ): ScriptIcon? =
            when (value) {
                in valueNames -> values().first { it.value == value }
                in enumNames -> valueOf(value)
                else -> null
            }

    }

